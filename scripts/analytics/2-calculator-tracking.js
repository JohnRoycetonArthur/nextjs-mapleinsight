// ── Step 4: PII audit ─────────────────────────────────────────────────────────

console.log('\n🔍  Running PII audit on dataLayer pushes...');

const PII_PATTERNS = [
  { re: /dataLayer\.push[\s\S]{0,200}email\s*:/,   label: 'email address in dataLayer push' },
  { re: /dataLayer\.push[\s\S]{0,200}income\s*:\s*[^'"a-z]/, label: 'raw income number in dataLayer push (use income_range instead)' },
  { re: /dataLayer\.push[\s\S]{0,200}name\s*:/,    label: 'name field in dataLayer push' },
  { re: /dataLayer\.push[\s\S]{0,200}sin\s*:/,     label: 'SIN field in dataLayer push' },
];

const SRC_DIRS = ['src', 'app', 'pages', 'components'].filter(d =>
  fs.existsSync(path.join(CWD, d))
);

let piiHits = 0;

function auditDir(dir) {
  let entries;
  try { entries = fs.readdirSync(dir, { withFileTypes: true }); }
  catch { return; }

  for (const entry of entries) {
    if (['node_modules','.next','.git','analytics-patches'].includes(entry.name)) continue;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      auditDir(full);
    } else if (['.ts','.tsx','.js','.jsx'].includes(path.extname(entry.name))) {
      // Skip the analytics utility itself — it contains the patterns intentionally
      if (entry.name === 'analytics.ts' || entry.name === 'analytics.js') continue;
      const content = fs.readFileSync(full, 'utf8');
      for (const { re, label } of PII_PATTERNS) {
        if (re.test(content)) {
          console.log(`  ⚠️   POTENTIAL PII: ${path.relative(CWD, full)}`);
          console.log(`          → ${label}`);
          piiHits++;
        }
      }
    }
  }
}

SRC_DIRS.forEach(d => auditDir(path.join(CWD, d)));

