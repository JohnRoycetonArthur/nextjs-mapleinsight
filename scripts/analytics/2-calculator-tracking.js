// ── dataLayer push helper ─────────────────────────────────────────────────────

function push(eventData: Record<string, unknown>): void {
  if (typeof window === 'undefined') return; // SSR guard
  window.dataLayer = window.dataLayer || [];
  window.dataLayer.push(eventData);
}

// ── Income binning (AC-5: no raw values ever sent to GA4) ────────────────────

export function getIncomeRange(income: number | string | null | undefined): IncomeRange {
  if (income === null || income === undefined || income === '') return 'not_provided';
  const n = Number(income);
  if (isNaN(n))     return 'not_provided';
  if (n < 20000)    return 'under_$20K';
  if (n < 40000)    return '$20K–$40K';
  if (n < 60000)    return '$40K–$60K';
  if (n < 80000)    return '$60K–$80K';
  if (n < 100000)   return '$80K–$100K';
  if (n < 150000)   return '$100K–$150K';
  if (n < 200000)   return '$150K–$200K';
  return 'over_$200K';
}

// ── Calculator events (US-8.2) ────────────────────────────────────────────────

/**
 * Fire when a user first changes any input in a calculator.
 * Use a ref guard to ensure this fires only ONCE per calculator session.
 *
 * @example
 * const startedRef = useRef(false);
 * <input onChange={() => trackCalculatorStarted('tfsa_vs_rrsp', startedRef)} />
 */
export function trackCalculatorStarted(
  calculatorName: CalculatorName,
  firedRef: React.MutableRefObject<boolean>
): void {
  if (firedRef.current) return; // Only fire once per session
  firedRef.current = true;
  push({ event: 'calculator_started', calculator_name: calculatorName });
}

/**
 * Fire when a user clicks calculate and a result renders.
 * Income is automatically binned — never pass raw values to GA4.
 *
 * @param calculatorName  - Which calculator (use CalculatorName type)
 * @param province        - Province string from selector, or null if not selected
 * @param annualIncome    - Raw income figure (will be binned automatically)
 */
export function trackCalculatorResult(
  calculatorName: CalculatorName,
  province: string | null | undefined,
  annualIncome: number | string | null | undefined
): void {
  push({
    event: 'calculator_result_viewed',
    calculator_name: calculatorName,
    province: province || 'not_selected',
    income_range: getIncomeRange(annualIncome), // ← always binned
  });
}

/**
 * Fire when a user SUCCESSFULLY submits the "email me these results" form.
 * Call this ONLY after a confirmed API success response — not on button click.
 * Do NOT include the email address in this call.
 */
export function trackResultEmailed(calculatorName: CalculatorName): void {
  push({ event: 'calculator_result_emailed', calculator_name: calculatorName });
}

// ── Checklist events (US-8.3) ─────────────────────────────────────────────────

/**
 * Fire on every check/uncheck of a checklist item.
 * Called inside handleToggle in ChecklistPage.
 */
export function trackChecklistItemChecked(
  itemId: string,
  itemPeriod: string,
  action: 'checked' | 'unchecked'
): void {
  push({ event: 'checklist_item_checked', item_id: itemId, item_period: itemPeriod, action });
}

/**
 * Track milestone completions (25%, 50%, 75%, 100%).
 * Pass the Set of already-reached milestones to avoid re-firing.
 * Mutates milestonesSet to record newly-reached milestones.
 */
export function trackChecklistMilestone(
  completedCount: number,
  totalCount: number,
  milestonesSet: Set<number>
): void {
  const MILESTONES = [25, 50, 75, 100];
  const pct = Math.round((completedCount / totalCount) * 100);

  for (const milestone of MILESTONES) {
    if (pct >= milestone && !milestonesSet.has(milestone)) {
      milestonesSet.add(milestone);
      push({ event: 'checklist_milestone_reached', milestone_percent: milestone });
      if (milestone === 100) {
        push({ event: 'checklist_completed' }); // GA4 Conversion event
      }
    }
  }
}

// ── Newsletter events (US-8.3) ────────────────────────────────────────────────

/**
 * Fire on successful newsletter form submission.
 * Call ONLY after confirmed API success — not on button click.
 * Do NOT pass the email address.
 *
 * @param placement - Where on the site the form was submitted
 */
export function trackNewsletterSignup(placement: SignupPlacement): void {
  push({ event: 'newsletter_signup', signup_placement: placement });
}

